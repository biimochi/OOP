public class Bai2_Chuong2 {
    public static void main(String[] args) {
        //a
        int a = 2;
        int b = 3;
        int c = 4;
        System.out.println(a < b || c > b);
        System.out.println("");

        //b
        System.out.println(a < b && c > b);
        System.out.println("");

        //c
        System.out.println(a < b && c < b);
        System.out.println("");

        //d
        System.out.println(!(a > c));
        System.out.println("");

        //e
        System.out.println(a == b);
        System.out.println("");

        //f
        System.out.println(c != b);
    }
}
