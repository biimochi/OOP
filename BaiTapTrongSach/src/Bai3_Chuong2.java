public class Bai3_Chuong2 {
    public static void main(String[] args) {
        int a = 10;
        int b = 100;
         //a
        System.out.println(a | b);

        //b
        System.out.println(a & b);

        //c
        System.out.println(a ^ b);

        //d
        System.out.println(~a);

        //e
        System.out.println(a >> 2);

        //f
        System.out.println(b << 2);
    }
}
